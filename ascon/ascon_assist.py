def ascon_linear(s,pos):
    #ascon的线性层一行s输入，求输出值
    rotate=[[0,19,28],[0,61,39],[0,1,6],[0,10,17],[0,7,41]]
    a=[]
    for i in range(64):
        a.append(eval(s[i]))
    a1=operation.roate_right(a,rotate[pos][1])
    a2=operation.roate_right(a,rotate[pos][2])
    for i in range(64):
        if 2 in [a[i],a1[i],a2[i]]:
            a[i]=2
        else:
            a[i]=a[i]^a1[i]^a2[i]
    ds=''
    for i in range(64):
        ds+=str(a[i])
    return ds


